#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int i;

	if (s && fd)
	{
		i = 0;
		while (s[i] != '\0')
		{
			ft_putchar_fd(s[i], fd);
			i++;
		}
		ft_putchar_fd('\n', fd);
	}
}
